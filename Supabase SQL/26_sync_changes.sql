-- Design contract / 设计约定:
--   1) One completed business action = one sync_changes row.
--      一次完整业务动作 = 一条 sync_changes 记录。
--   2) event_id must be idempotent and unique (client-side generated).
--      event_id 必须可幂等且唯一（客户端生成）。
--   3) Business write + sync_changes insert must be in the same DB transaction.
--      业务写入 + sync_changes 插入必须在同一数据库事务中提交。
