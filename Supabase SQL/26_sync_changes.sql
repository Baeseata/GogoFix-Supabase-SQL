-- =============================================
-- ENUM: sync_event_type — Business event classification
-- 枚举：sync_event_type — 业务事件分类
-- =============================================
DO $$
BEGIN
  CREATE TYPE public.sync_event_type AS ENUM (
    'transaction_committed',
    'inventory_adjustment_committed',
    'purchase_order_received',
    'store_transfer_committed',
    'store_transfer_received',
    'repair_ticket_committed',
    'customer_updated',
    'supplier_updated',
    'misc_updated'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;

-- =============================================
-- Function: block_sync_changes_mutation()
-- 函数：block_sync_changes_mutation()
-- =============================================
-- sync_changes is append-only; UPDATE/DELETE are blocked.
-- sync_changes 为追加型日志；禁止 UPDATE / DELETE。
CREATE OR REPLACE FUNCTION public.block_sync_changes_mutation()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  RAISE EXCEPTION 'sync_changes is append-only; % is not allowed', TG_OP;
END;
$$;

-- =============================================
-- Table creation / 建表
-- =============================================
CREATE TABLE IF NOT EXISTS public.sync_changes (

  -- Global increasing cursor used by clients for pull-based incremental sync
  -- 全局递增游标，供客户端拉取增量同步使用
  change_id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,

  -- Idempotency key generated by client/local outbox event
  -- 幂等键，由客户端/本地 outbox 事件生成
  event_id text NOT NULL,

  -- Correlation key linking related events in one workflow chain
  -- 关联键，用于把同一业务链路的多条事件串联
  correlation_id text DEFAULT NULL,

  -- Business event type
  -- 业务事件类型
  event_type public.sync_event_type NOT NULL,

  -- One event may involve multiple primary entity IDs
  -- 一条事件可能涉及多个主实体 ID
  entity_ids text[] NOT NULL DEFAULT '{}'::text[],

  -- Target store that should consume this event; NULL = global event
  -- 目标门店（需要消费该事件）；NULL = 全局事件
  target_store_id text DEFAULT NULL REFERENCES public.store_list(store_id),

  -- Source store that produced this event
  -- 事件来源门店
  source_store_id text NOT NULL REFERENCES public.store_list(store_id),

  -- Source device identifier (text only; no FK by design)
  -- 来源设备标识（仅 text；按设计不加 FK）
  source_device_id text DEFAULT NULL,

  -- Event payload packet (recommended keys: event_type/entity_ids/store_scope/data/meta)
  -- 事件载荷（建议键：event_type/entity_ids/store_scope/data/meta）
  payload_json jsonb NOT NULL,

  -- Client-side business occurrence time
  -- 客户端业务发生时间
  occurred_at timestamptz NOT NULL DEFAULT now(),

  -- Cloud commit time when this event row is persisted
  -- 云端入库提交时间
  committed_at timestamptz NOT NULL DEFAULT now(),

  -- CHECK: event_id must not be blank
  -- 约束：event_id 不可为空白字符串
  CONSTRAINT chk_sync_changes_event_id_not_blank
    CHECK (length(btrim(event_id)) > 0),

  -- CHECK: source_store_id must not be blank
  -- 约束：source_store_id 不可为空白字符串
  CONSTRAINT chk_sync_changes_source_store_not_blank
    CHECK (length(btrim(source_store_id)) > 0),

  -- CHECK: payload_json should be a JSON object
  -- 约束：payload_json 应为 JSON 对象
  CONSTRAINT chk_sync_changes_payload_is_object
    CHECK (jsonb_typeof(payload_json) = 'object')
);

