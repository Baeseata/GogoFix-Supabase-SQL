ALTER TABLE public.sync_changes
  ADD COLUMN IF NOT EXISTS correlation_id text;

ALTER TABLE public.sync_changes
  ADD COLUMN IF NOT EXISTS occurred_at timestamptz NOT NULL DEFAULT now();

ALTER TABLE public.sync_changes
  ADD COLUMN IF NOT EXISTS committed_at timestamptz NOT NULL DEFAULT now();

-- =============================================
-- Idempotency uniqueness / 幂等唯一性
-- =============================================
CREATE UNIQUE INDEX IF NOT EXISTS uq_sync_changes_event_id
  ON public.sync_changes (event_id);

-- =============================================
-- Indexes / 索引
-- =============================================

-- Main pull path: target store + cursor
-- 主要拉取路径：目标门店 + 游标
CREATE INDEX IF NOT EXISTS idx_sync_changes_target_store_change_id
  ON public.sync_changes (target_store_id, change_id);

-- Source tracing: source store + cursor
-- 来源追踪：来源门店 + 游标
CREATE INDEX IF NOT EXISTS idx_sync_changes_source_store_change_id
  ON public.sync_changes (source_store_id, change_id);

-- Commit-time descending timeline
-- 提交时间倒序时间线
CREATE INDEX IF NOT EXISTS idx_sync_changes_committed_at
  ON public.sync_changes (committed_at DESC);

-- Correlation lookup
-- 关联键查询
CREATE INDEX IF NOT EXISTS idx_sync_changes_correlation_id
  ON public.sync_changes (correlation_id)
  WHERE correlation_id IS NOT NULL;

-- Event type filtering
-- 按事件类型筛选
CREATE INDEX IF NOT EXISTS idx_sync_changes_event_type_change_id
  ON public.sync_changes (event_type, change_id);

-- Entity membership lookup
-- 实体 ID 数组包含关系查询
CREATE INDEX IF NOT EXISTS gin_sync_changes_entity_ids
  ON public.sync_changes USING gin (entity_ids);

-- =============================================
-- Triggers: block UPDATE/DELETE (append-only log)
-- 触发器：阻止 UPDATE/DELETE（追加型日志）
-- =============================================
DROP TRIGGER IF EXISTS trg_sync_changes_block_update ON public.sync_changes;
CREATE TRIGGER trg_sync_changes_block_update
BEFORE UPDATE ON public.sync_changes
FOR EACH ROW
EXECUTE FUNCTION public.block_sync_changes_mutation();

DROP TRIGGER IF EXISTS trg_sync_changes_block_delete ON public.sync_changes;
CREATE TRIGGER trg_sync_changes_block_delete
BEFORE DELETE ON public.sync_changes
FOR EACH ROW
EXECUTE FUNCTION public.block_sync_changes_mutation();
